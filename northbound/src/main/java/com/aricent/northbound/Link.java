package com.aricent.northbound;

public class Link {
                private String source;
                private String target;
                public String getSource() {
                                return source;
                }
                public Link setSource(String source) {
                                this.source = source;
                                return this;
                }
                public String getTarget() {
                                return target;
                }
                public Link setTarget(String target) {
                                this.target = target;
                                return this;
                }
                

}
